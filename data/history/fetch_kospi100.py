def main():
    total = len(KOSPI_100)
    ok = 0
    fail = 0
    total_rows = 0

    print(f"KOSPI 100 종목 히스토리 수집 시작 ({total}종목)")
    print("=" * 60)

    for i, (code, name) in enumerate(KOSPI_100.items(), 1):
        ticker = f"{code}.KS"
        csv_path = OUT_DIR / f"{code}_{name.replace('/', '_')}.csv"

        print(f"  [{i}/{total}] {name}({code})...", end=" ", flush=True)

        try:
            data = yf.download(ticker, period="max", progress=False, timeout=15)
            if data.empty:
                print("데이터 없음")
                fail += 1
                continue

            if isinstance(data.columns, pd.MultiIndex):
                data.columns = data.columns.get_level_values(0)

            cols = [c for c in ["Open", "High", "Low", "Close", "Volume"] if c in data.columns]
            data = data[cols].dropna(how="all")
            data.to_csv(csv_path)

            years = (data.index[-1] - data.index[0]).days / 365.25 if len(data) > 1 else 0
            print(f"{len(data)}행 ({years:.1f}년)")
            ok += 1
            total_rows += len(data)

        except Exception as e:
            print(f"실패: {e}")
            fail += 1

        time.sleep(0.3)

    print("=" * 60)
    print(f"완료: {ok}/{total} 성공, {fail} 실패, 총 {total_rows:,}행")


if __name__ == "__main__":
    main()
